const socialIcons = {
  linkedin: LinkedInIcon,
  twitter: TwitterIcon,
  github: GitHubIcon,
  instagram: InstagramIcon,
  reddit: RedditIcon,
  medium: MediumIcon,
}

function SocialLink({ platform, url }) {
  const Icon = socialIcons[platform.toLowerCase()]
  if (!Icon) return null

  return (
    <a
      href={url}
      target="_blank"
      rel="noopener noreferrer"
      className="text-zinc-500 hover:text-zinc-700 dark:text-zinc-300 dark:hover:text-white transition-colors"
      aria-label={`${platform} profile`}
    >
      <Icon className="h-5 w-5" />
    </a>
  )
}

export const Author = memo(function Author({ author }) {
  if (!author) return null

  return (
    <div className="flex items-center gap-4 mb-8">
      <div className="flex-shrink-0">
        <Image
          src={author.image}
          alt={author.name}
          width={64}
          height={64}
          className="rounded-full object-cover w-16 h-16"
        />
      </div>
      <div className="flex-1 min-w-0 h-16 flex flex-col justify-between">
        <div className="flex-1 flex flex-col justify-center min-w-0">
          <h2 className="text-base font-semibold text-zinc-900 dark:text-white leading-tight">
            {author.name}
          </h2>
          {author.bio && (
            <p className="text-xs text-zinc-600 dark:text-zinc-400 leading-tight line-clamp-2 mt-0.5">
              {author.bio}
            </p>
          )}
        </div>
        {author.social && author.social.length > 0 && (
          <div className="flex gap-2 mt-1">
            {author.social.map((social) => (
              <SocialLink
                key={`${author.name}-${social.platform}`}
                platform={social.platform}
                url={social.url}
              />
            ))}
          </div>
        )}
      </div>
    </div>
  )
})

export function Authors({ authors }) {
  if (!authors || authors.length === 0) return null

  return (
    <div className="mb-8">
      {authors.map((author) => (
        <Author key={author.name} author={author} />
      ))}
    </div>
  )
}
